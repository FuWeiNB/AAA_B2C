package com.bbb.entity.zyh;

public class Emp {
    private int e_id;
    private String e_name;
    private String e_pass;
    private int r_id;
    private String e_flag;

    public int getE_id() {
        return e_id;
    }

    public void setE_id(int e_id) {
        this.e_id = e_id;
    }

    public String getE_name() {
        return e_name;
    }

    public void setE_name(String e_name) {
        this.e_name = e_name;
    }

    public String getE_pass() {
        return e_pass;
    }

    public void setE_pass(String e_pass) {
        this.e_pass = e_pass;
    }

    public int getR_id() {
        return r_id;
    }

    public void setR_id(int r_id) {
        this.r_id = r_id;
    }

    public String getE_flag() {
        return e_flag;
    }

    public void setE_flag(String e_flag) {
        this.e_flag = e_flag;
    }
}
